    <p class="indent4">9、当用户点击某个节点时，指向该节点的节点和连接线高亮显示,同时在右侧所使用资源区域的说明页签中显示当前节点的说明信息，当用户点击工作区空白区域时，若已有高亮显示的节点和线，则会取消高亮显示</p>
    <p>五、信息提示区</p>
    <p class="indent2">（一）介绍：该区域将图形化流程图中的结果信息展示给用户，包括数据结果集、执行结果、缩略图</p>
