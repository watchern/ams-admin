    <p>一、页面布局：主页一共分为五个区域，即左侧的资源列表区、顶部的功能区、中间区域的工作区、底部的信息提示区、右侧的所使用资源区。</p>
    <p>二、资源列表区</p>
    <p class="indent2">（一）介绍：该区域展示的是用户用以创建图形化流程图的原表树形列表，原表类型主要包括数据表（业务数据表、项目数据表、个人数据表）、模型结果表、业务维度表和风险查证模型表。</p>
    <p class="indent2">（二）操作</p>
    <p class="indent4">1、用户可对树列表的表节点进行拖拽操作，将选择的表拖拽至中间区域的工作区中生成一个原表节点</p>
    <p class="indent4">2、用户可对树列表中的单个表节点进行右键【预览数据】、【关联表查询】的操作</p>
    <p class="indent4">3、用户可对树列表中的根节点进行右键【刷新】的操作</p>
    <%--<p class="indent4">3、用户可对树列表中的多个表节点进行Ctrl多选右键【合并】/【关联】操作，此时中间区域的工作区中会自动生成所选择树节点的图形（原表）和操作图形（【数据合并】/【数据关联】）</p>--%>
    <%--<p class="indent4">4、用户在搜索框内输入要搜索的内容，可对所有节点的内容进行搜索，查找符合条件的树节点，搜索精度到数据表的列</p>--%>
    <p>三、功能区</p>
    <p class="indent2">（一）介绍：该区域展示的是用户用以创建图形化流程图的处理表数据的功能节点和用以存储图形化流程图信息的功能按钮，其中功能节点包括：数据筛选、数据排序、数据抽样、数据分层、分组汇总、数据去重、数据频次分析、数据转码、SQL查询器、数据融合、数据关联，功能按钮包括：打开、分享、保存为、另存为、撤销、恢复、全部运行、帮助，以及【文件】区域的更多功能（包括图形导入、图形导出、数据导入）</p>
    <p class="indent2">（二）操作</p>
    <p class="indent4">1、功能节点：用户可对功能节点进行拖拽操作，将选择的节点拖拽至中间区域的工作区中生成一个对应图形的操作节点</p>
    <p class="indent4">2、功能按钮：</p>
    <p class="indent6">（1）新建：浏览器中打开新的图形化窗口，用户可创建新的图形化流程图</p>
    <p class="indent6">（2）打开：当前窗口中弹出小窗口选择一条图形化记录后，在浏览器中打开的新窗口中加载选择的图形化流程图，用户可在选择的流程图上继续操作</p>
    <p class="indent6">（3）保存：用户保存当前操作的图形化流程图</p>
    <p class="indent6">（4）另存为：用户可将当前操作的图形化流程图另存储一份进行保存</p>
    <p class="indent6">（5）帮助：提供给用户具体操作图形化工具的帮助说明</p>
