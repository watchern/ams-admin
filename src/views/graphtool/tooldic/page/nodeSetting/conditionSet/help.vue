    <p class="indent2">（一）介绍：该区域主要是用户用以图形化流程图的设计区域，所有的流程节点都会被拖拽至此区域进行设计，流程图中只包含节点和连接线（边），当设计的流程图过多超出当前工作区的区域时，工作区会自动向右、向下进行范围的扩充。</p>
    <p class="indent2">（二）操作</p>
    <p class="indent4">1、用户可根据需要拖动节点进行位置变动</p>
    <p class="indent4">2、鼠标移动至节点上，可点击【↓】拖动至另一个节点上，进行节点间的连线操作</p>
    <p class="indent4">3、鼠标点击连接线，拖动连接线的两端拖动至另一个节点上，变更节点间的连线</p>
    <p class="indent4">4、空白区域点击鼠标右键，出现右键菜单，包括选中节点、选中连线、选中所有、撤销、粘贴功能</p>
    <p class="indent6">（1）选中节点：选中当前流程图中所有的节点</p>
    <p class="indent6">（2）选中连线：选中当前流程图中所有的连接线</p>
    <p class="indent6">（3）选择所有：选中当前流程图中所有的节点和连接线</p>
    <p class="indent6">（4）撤销：撤销用户当前步骤的操作内容</p>
    <p class="indent6">（5）粘贴：粘贴用户当前剪切或复制的操作内容</p>
    <p class="indent4">5、节点点击鼠标右键，不同类型的节点出现不同的右键菜单，总的菜单包括执行、取消执行、查看数据、最终结果输出、查询执行语句、SQL编辑、配置节点属性、重置配置、重命名、复制、剪切、删除功能。</p>
    <p class="indent6">（1）执行本节点：结果表节点特有菜单项，执行处于未执行或执行失败状态的当前结果表节点</p>
    <p class="indent6">（2）执行到本节点：结果表节点特有菜单项，执行处于未执行或执行失败状态的当前结果表节点及其前置节点</p>
    <p class="indent6">（3）取消执行：结果表节点特有菜单项，取消处于正在执行状态的当前结果表节点及其前置节点的执行操作</p>
    <p class="indent6">（4）查看数据：原表和结果表节点的菜单项，查看当前节点中的数据</p>
    <p class="indent6">（5）最终结果输出：结果表节点特有的菜单项，标记当前结果表的节点为最终结果输出节点，在保存模型时以此节点的SQL语句作为模型的SQL语句</p>
    <p class="indent6">（6）查看SQL语句：除自定义图形节点外的其他节点的菜单项，用以查看当前节点执行成功后的SQL语句</p>
    <%--<p class="indent6">（7）SQL编辑：SQL查询器特有的菜单项，用来辅助用户自定义编写SQL语句和实时查询数据</p>--%>
    <p class="indent6">（7）XXXX设置：所有操作节点的菜单项，可对当前的操作节点进行配置设置，包括了节点的基础信息配置、个性化的高级配置和部分节点具有的输出字段配置</p>
